public class Arrays {
    public static void main(String[] args) {
        System.out.println("- Arrays Traversal -");

        int arr[] = {5, 3, 8, 6, 2};
        for (int k = 0; k < arr.length; k++) { //Gets all the contents of the array
            System.out.print(arr[k] + " "); // prints the contents of the array
        }   
    }
}
